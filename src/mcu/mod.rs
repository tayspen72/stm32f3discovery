use stm32f3::stm32f303;

pub mod gpio;

//==============================================================================
// Enums, Structs, and Types
//==============================================================================


//==============================================================================
// Variables
//==============================================================================


//==============================================================================
// Public Functions
//==============================================================================
pub fn init() {
	let peripherals = stm32f303::Peripherals::take().unwrap();

	gpio::init(
		peripherals.GPIOA,
		peripherals.GPIOB,
		peripherals.GPIOC,
		peripherals.GPIOD,
		peripherals.GPIOE,
		peripherals.GPIOF,
		peripherals.GPIOG,
		peripherals.GPIOH,
	);
}

//==============================================================================
// Private Functions
//==============================================================================


//==============================================================================
// Task Handler
//==============================================================================
pub fn task_handler() {
	gpio::task_handler();
}
